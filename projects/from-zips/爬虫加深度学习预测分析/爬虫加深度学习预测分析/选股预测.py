import pandas as pd
import os
# ---------------------- 数据解析与预处理 ----------------------
# 读取Excel文件（请根据实际路径修改文件路径）
file_path = "解决过拟合后的结果//所有公司选股预测汇总.xlsx"  # 示例路径，需替换为实际文件路径
df = pd.read_excel(file_path, sheet_name="Sheet1")

# 解析MSE列（字符串列表转数值列表，并计算平均值）
df["MSE"] = df["MSE"].apply(lambda x: eval(x))  # 安全转换字符串列表为数值列表
df["平均MSE"] = df["MSE"].apply(lambda x: sum(x)/len(x))  # 计算每个公司的平均MSE

# 解析R2列（字符串列表转数值列表，并计算平均值）
df["R2"] = df["R2"].apply(lambda x: eval(x))  # 安全转换字符串列表为数值列表
df["平均R2"] = df["R2"].apply(lambda x: sum(x)/len(x))  # 计算每个公司的平均R2


# ---------------------- 选股策略定义 ----------------------
# 定义选股条件（可根据业务需求调整阈值）
选股条件 = (
    (df["加权得分"] > 0.9)       # 加权得分高于0.9（综合表现优秀）
    & (df["平均MSE"] < 0.002)    # 平均预测误差（MSE）低于0.002（误差较小）
    & (df["平均R2"] > 0.9)       # 平均拟合度（R²）高于0.9（模型拟合效果好）
)

# 应用选股条件
精选股票池 = df[选股条件][["公司", "加权得分", "平均MSE", "平均R2"]].copy()


# ---------------------- 结果输出与保存 ----------------------
# 生成策略说明文本
策略说明 = """
============== 多指标量化选股策略 ==============
1. 加权得分筛选：选择综合得分高于0.9的公司，代表模型预测综合表现优异
2. 预测误差控制：平均MSE（均方误差）低于0.002，确保预测结果稳定性
3. 模型拟合度要求：平均R²高于0.9，说明模型对历史数据拟合效果良好
（注：MSE越小表示预测误差越小，R²越接近1表示拟合度越高）

============== 最终选股结果 ==============
共筛选出{0}家符合条件的公司：
""".format(len(精选股票池))

# 拼接具体股票信息
股票详情 = "\n".join([
    f"公司名称：{row['公司']}\n"
    f"加权得分：{row['加权得分']:.4f}\n"
    f"平均MSE：{row['平均MSE']:.6f}\n"
    f"平均R²：{row['平均R2']:.4f}\n"
    f"------------------------"
    for _, row in 精选股票池.iterrows()
])

# 合并完整输出内容
输出内容 = 策略说明 + 股票详情

# 保存结果到txt文件
output_file = "解决过拟合后的结果//最终选股策略.txt"  # 可修改保存路径
if not os.path.exists(os.path.dirname(output_file)):
    os.makedirs(os.path.dirname(output_file))
with open(output_file, "w", encoding="utf-8") as f:
    f.write(输出内容)

# 控制台打印提示信息
print(f"选股策略执行完成！结果已保存至：{output_file}")
print(f"筛选结果概览：\n{精选股票池[['公司', '加权得分']]}")